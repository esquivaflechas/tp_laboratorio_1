/** \brief Suma dos numeros entre ellos
 *
 * \param primer numero
 * \param segundo numero
 * \return el resultado de la suma
 *
 */
int suma(int , int );

/** \brief resta el segundo operador al primero
 *
 * \param el minuendo de la resta
 * \param el substraendo de la resta
 * \return el resultado de la resta
 *
 */

int resta(int , int );

/** \brief Multiplica dos numeros entre si
 *
 * \param El primer numero que se multiplica
 * \param El segundo numero que se multiplica
 * \return El resultado de la multiplicacion
 *
 */

int multiplicacion(int , int );

/** \brief Se divide el primer operador por el segundo
 *
 * \param el divisor de la cuenta
 * \param el dividendo de la cuenta
 * \return El resultado de la division entre los dos operadores
 *
 */

int division(int , int );

/** \brief Realiza el factorial del parametro entregado
 *
 * \param Numero que se va a factorizar
 * \return El resultado del factorial del numero
 *
 */

int factorial(int );
